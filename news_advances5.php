<b>Anti-angiogenesis drugs</b><br>
Cancers need to make more blood vessels in order to grow. This process of  is called angiogenesis. Drugs that block angiogenesis have been identified and are being tested in laboratory animals and humans.<br><br>

<b>Monoclonal antibodies</b><br>
Antibodies allow our bodies to get rid of cancer cells. They act by attaching like a puzzle piece to certain types of proteins (antigens) which lie on the outer surface of cancer cells, thereby killing the cell. <br><br>

Scientists are experimenting with attaching anti-cancer drugs to antibodies which will then seek out the antigens on cancer cells. When the antibodies and anti-cancer drugs attach to the cancer cells, the cancer cells will be killed. The anti-cancer drugs only go to the cancerous cells and side effects are avoided.<br><br>



Watching your serving size is one of the best ways to control calories and reach or maintain a healthy weight. When you've got a big plate piled with food in front of you, it's very easy to eat too much. Read on to learn more about serving sizes.<p>

<img src="images/tipsheets/serving_size.jpg" width=345 height=197 border=0 alt=""><br><br>


<b>What is a serving?</b>

<ul>
<li><b>Vegetables:</b> 1 cup of lettuce, 1/2 cup cooked vegetables</li>
<li><b>Fruit:</b> 1 medium piece or 1/2 cup canned fruit, 6 ounces fruit juice</li>
<li><b>Whole grains:</b> 1 slice bread, 3/4 cup dry cereal, 1/2 cup cooked cereal or rice</li>
<li><b>Low-fat dairy:</b> 1 cup milk, 1 cup yogurt, 1/2 cup cottage cheese, 1.5 ounces low-fat cheese</li>
<li><b>Protein:</b> 2 to 3 ounces of meat, fish, or chicken (1/2 cup beans, one egg, or 1/3 cup nuts count as 1 ounce of meat)</li>
</ul>

<b>Here's how serving sizes compare to some everyday things:</b><p>

1/4 cup = 1 golf ball<br>
1/2 cup = 1/2 baseball<br>
1 cup = 1 baseball<br>
3 ounces of meat = 1 deck of cards<br>
1 ounce of cheese = 4 dice<p>

<b>How many servings do I need each day?</b><p>

5 to 9 servings of fruits and vegetables<br>
3 to 5 servings of whole grains<br>
2 to 3 servings of protein such as fish, chicken, eggs, beans, or nuts<br>
2 to 3 servings of low-fat dairy such as yogurt or skim milk<p>
Note: Your number of servings of healthy fats such as olive oil and canola oil will depend on how many extra calories you can eat without gaining weight.









There have been major advancements in cancer treatment over the last several decades, and that means that childhood cancer survivors are a large and ever-increasing group. But these treatments can have late effects. These are the kinds of side effects that happen long after treatment ends. For survivors who are smokers, the effects of smoking combined with late effects create many health risks.<br><br>
Partnership for Health, or PFH, has helped survivors in the past to learn how to live the healthiest lives possible. For survivors who wanted to quit smoking, PFH provided resources and telephone counseling to help them achieve that goal.<br><br>
By creating this Web site, Partnership for Health now aims to reach even more survivors. The site will provide all the information and assistance the earlier project did, but in online, interactive form. The goals are the same: to help survivors make health changes that can lower their risk of future health problems, to help them understand their unique risks of smoking, and to provide support and assistance if and when they want to quit.<br><br>

<b>What will I be asked to do if I join Partnership for Health?</b><br><br>
We will ask you to complete a phone survey before accessing this Web site. You will also be asked to:
<ul>
<li>complete a short email survey 6 months after you first access the Web site
<li>complete a phone survey 18 months after you first access the Web site
<li>visit and explore the Web site as often as possible
</ul>

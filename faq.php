<a name="receive"></a><br>
<div align="center"><img src="images/ruleLeft.gif" width=280 height=2 border=0 alt=""></div><br><br>
<b>What will I receive as part of Partnership for Health?</b><br><br>
You will not receive any medical benefits by taking part in this study, because we cannot give your individual results to you (or anyone else). You may receive potential health benefits if you stop smoking. If you choose to, you have the option to receive a dose-appropriate amount of free <a href="health_pharmo.php">quit-smoking medication</a> (Zyban or the patch).<br><br>
By taking part in this study, you will also play an important part in helping us learn how to create health programs for childhood cancer survivors.<br><br>

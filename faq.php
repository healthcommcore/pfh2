<b>How will my privacy be protected?</b><br><br>
All information you provide will be strictly confidential. It will be coded with an identification number, and your name will not be used. The information will not be shared with anyone outside of the research staff, including your doctor or any members of the medical team participating in your care. Visit our <a href="policies1.php">policy</a> section for more information.<br><br>

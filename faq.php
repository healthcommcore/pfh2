<div align="center"><img src="images/ruleLeft.gif" width=280 height=2 border=0 alt=""></div><br><br>
<b>Who can join?</b><br><br>
You are eligible to join if you:
<ul>
<li>were diagnosed with childhood cancer or a similar illness before age 21</li>
<li>are presently between the ages of 18 and 45</li>
<li>are not currently being treated for cancer</li>
<li>are at least two years out of your most recent treatment</li>
<li>are mentally capable of giving verbal consent</li>
<li>can read and speak English</li>
<li>are reachable by phone for consent and surveys (a preliminary survey and a follow-up survey 12 months later)</li>
<li>smoke</li>
</ul>

<span class="manualHeader">QUESTIONS AND ANSWERS ABOUT NICOTINE PATCHES</span><br><br>

<b>How does the nicotine patch work?</b><br>
When a smoker quits smoking, his or her body and brain may crave the nicotine they have become used to. The nicotine patch releases nicotine into the body through the skin, but allows the smoker to slowly lower the amount of nicotine in his or her body until he or she is nicotine-free.<br><br> Unlike cigarettes, nicotine patches do not contain tar or carbon monoxide. The patch has a lower amount of nicotine than cigarettes, and it helps with withdrawal symptoms. Most smokers stay on the patch for 6 to 10 weeks.<br><br>

<b>PLEASE NOTE:</b> You should not smoke or use any other nicotine-containing products (snuff, chewing tobacco, or nicotine gum) while using the patch.<br><br>

<b>How effective is the patch?</b><br>
The patch <b>doubles</b> the chances that you'll quit smoking for good.<br><br>

<b>What can I expect when I quit smoking?</b><br>
You might experience nicotine withdrawal. This is usually most intense during the first 3 to 4 days after quitting. Some people have cravings for nicotine, feel anxious or irritable, or have trouble concentrating. The good news is that the nicotine patch reduces many of these symptoms, and helps make quitting much easier! Also, Partnership For Health will help you by sharing tips and information on how to work through some of these challenges.<br><br>

<b>What are the side effects to using the patch?</b><br>
It's normal to have some mild itching, burning, or tingling when you first apply the patch. This usually goes away within an hour. People who are allergic to adhesive tape or who have skin problems may have some minor skin irritation or a rash where they apply the patch. Some have vivid dreams or nightmares while using the patch. If this happens, you should not sleep with the patch on. Try putting one on first thing in the morning and take it off before going to bed.<br><br>

<b>Is the patch right for me?</b><br>
Most people can use the patch without any problems. But there are some medical conditions that rule out its use. People who have active or uncontrolled heart disease or thyroid conditions should not use it. Because nicotine aggravates ulcers, people with peptic ulcer disease should not use it either. Talk to your doctor about your health history. She or he will be able to help you decide if NRT is right for you.<br><br>

<b>How much does the patch cost?</b><br>
Nothing! As a member of Partnership for Health, you may request a free 8- to 10-week supply.* You and your doctor will decide on the right dose for you.<br><br>

<b>How can I get the patch?</b><br>
Read and fill out the consent form to request nicotine patches. We will contact your doctor to make sure the patch is right for you. If your doctor agrees, we will send him or her the supply of nicotine patches. You will need to go to your doctor to pick them up.<br><br>


*GlaxoSmithKline is donating the NRT to Partnership for Health.<br><br>









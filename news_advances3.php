<span class="manualHeader">ADVANCES IN RADIATION THERAPY</span><br><br>

Advances in this area are being made by limiting the amount of radiation and changing the way it is delivered. Lessening the amount of radiation may mean fewer problems with late effects in organs that received radiation.<br><br>

Brachytherapy, also called seed radiation, is a new method of delivering radiation from inside the body.  With this method, doctors insert several small needles directly into the area of the tumor to be radiated. This limits the area of body exposed to radiation. It also shortens the treatment time.<br><br>

Stereotactic radiosurgery is a method designed to deliver radiation doses to small treatment areas, meaning less damage to nearby normal tissue. It is particularly useful in treating sensitive areas, such as the brain.<br><br>

Bone marrow transplantation can be used to treat leukemia, lymphoma, Hodgkin's disease, and other types of cancer that have not responded to traditional therapies. The bone marrow contains stem cells, which play a crucial role in the body. Stem cells are the beginnings of all of the cells in our bodies. By replacing these cells in the bone marrow, the body can rebuild itself after receiving very high amounts of radiation or chemotherapy. <br><br>

Here's how bone marrow transplantation works:
<ul>
<li>Before radiation or chemotherapy, a matched donor is found or some of the patient's own marrow is removed and cleansed.</li>
<li>Total body irradiation and high doses of chemotherapy are given, which kill cancer cells. This intensive radiation and chemotherapy also destroys the patient's bone marrow.</li>
<li>The marrow is replaced with the marrow from a matched donor or the patient's own marrow. The body begins the process of rebuilding itself with healthy cells.</li>
</ul>

Scientists are also trying to develop ways to detect damage to organs before symptoms appear. With this ability, physicians will be able to treat patients with the highest tolerable dose without damaging organs unnecessarily.<br><br>




When a boy or girl is diagnosed with cancer, the oncologists' first priority is to save the life of the child. Doctors use everything available to them &#150; chemotherapy, radiation, and surgery &#150; to cure the cancer. But although these therapies are life-saving, they can also cause late effects. Late effects are side effects that are lasting &#150; sometimes for a person's whole life.<br>

<p>Some late effects of cancer treatment are more severe than others. But many types of late effects can be made much worse by smoking. Childhood cancer survivors who smoke may not know that they face special kinds of risks by smoking. Read on to learn more about the relationship between late effects and smoking.</p>

Common chemotherapy drugs that can damage the heart and lungs include:

<ul>

<li>bleomycin (lungs)</li>
<li>carmustine, also called BCNU (lungs)</li>
<li>daunorubicin, also called daunomycin (heart)</li>
<li>doxorubicin, also called adriamycin (heart)</li>
<li>idarubicin (heart)</li>
<li>lomustine, also called CCNU (lungs)</li>
</ul><br>

These drugs can all cause problems with the way the heart and lungs work. When smoking is added to the mix, the risk of problems increases.<br>

<p>Smoking can also worsen the late effects of radiation, especially radiation to the chest and spine, or total body irradiation (TBI). Total body irradiation is often given before a bone marrow transplant. Although it is life-saving at the time, radiation can set the stage for a new cancer in someone who smokes later in life. This is even if the first cancer wasn't in the lungs or even related to smoking. And most smoking-related new cancers aren't nearly as curable as many childhood cancers.</p>

<p>Quitting smoking means taking a big step toward protecting your health. It can help you avoid a new cancer in the future, and will prevent certain late effects from becoming worse. And quitting also means protecting those around you by not exposing them to <a href="health_tips-2ndHand.php">secondhand smoke</a>!</p>






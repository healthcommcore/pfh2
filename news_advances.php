Many childhood cancer survivors are interested in the history of treatment and the advances that have been made in recent years.<br><br>

The first effective chemotherapy treatment for childhood cancer was discovered in the 1940s by Dr. Sidney Farber. Since then, tremendous improvements have been made in the treatment of childhood cancers. Today, nearly 80% of childhood cancer survivors are expected to pass the five-year survival mark.<br><br>

Some major accomplishments have been made thanks to chemotherapy. Chemotherapy is carried by the bloodstream to almost every part of the body. This means that cancer cells can be destroyed even if they have left the original site of the cancer. In the treatment of solid tumors, the use of combination therapies  such as chemotherapy along with surgery or radiation therapy has also been very important. <br><br>

Over the last several years, there have also been breakthroughs in the understanding of the basic biology of cancer. Cancers grow because of a slow gathering of changes in one of the trillions of cells in the body  in the DNA, or instructions that guide how the cell behaves. Scientists now know how genes can stimulate and hold back the growth of cells. Their knowledge of how cancer cells work will guide future research in the effort to improve prevention, detection, and treatment of cancer.<br><br>

Understanding the genes that are linked to cancer has also allowed scientists to explore how to prevent, find, and treat cancer. As their knowledge of genes grows, their ability to diagnose and treat cancer will continue to grow.<br><br>

Researchers and doctors are especially concerned about preserving the quality of life for a child who has cancer. Limb-sparing surgery for many sarcomas has had a major impact. Medicines have been developed to help protect major organs from damage caused by cancer drugs. Click on the links below to learn about other improvements.<br><br>




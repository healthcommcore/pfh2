<img src="images/tipsheets/mens-fertility.jpg" width=172 height=184 border=0 alt="" align="right" style="padding-left:20px; padding-top:3px; padding-bottom:16px;">
Although radiation, chemotherapy, and surgery can save the life of someone with childhood cancer or a similar illness, these treatments can sometimes affect a man's fertility later on. Because each situation is unique, the way that radiation, chemotherapy, and surgery affect the reproductive system can be different from one man to another. <br><br>

A man's testes are responsible for making sperm. They also make and maintain the right levels of male hormones. If the testes are harmed during radiation or chemotherapy, there may be late effects like <b>azoospermia</b> and <b>oligospermia</b>. <b>Azoospermia</b> is the name for when the testes stop making sperm entirely. <b>Oligospermia</b> occurs when the testes still make sperm, but the number of sperm is reduced. <br><br>

The late effects of radiation on fertility depend on the dose and location. The more radiation, the more fertility may be reduced. In some men, the effects of radiation can be reversible and fertility problems get better with time. The effects of chemotherapy depend on the dose and type. The higher the total dose, the more problems there may be with fertility.<br><br>

Certain parts of the brain are required for the testes to produce sex hormones.  Surgery to these parts of the brain can result in low levels of testosterone, the hormone that's necessary for sperm to be produced. Surgery that involves the testes can also cause impotence or problems with ejaculation.<br><br>

<br>
<b>Smoking and Fertility</b><br><br>

Smoking may lower a man's sperm count and affect the quality of the sperm. If you are prone to these types of treatment effects <b>and</b> you smoke, you may be affecting your fertility even more.<br><br>

It is important to talk about the details of your treatment with your doctor, to figure out if your medical history may contribute to problems with fertility.<br><br>


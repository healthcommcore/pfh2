Even if you eat many kinds of foods, you may not get all the nutrients you need to be healthy. Nutrients are the vitamins and minerals in food that the body uses to stay healthy. Multivitamins can help give you some of these nutrients. Multivitamins also have folic acid, which helps to prevent birth defects and colon cancer.<p>


<b>Q: So if I don't eat healthy, a multivitamin will make it OK?</b><br>
A: No. Multivitamins do not replace nutrients that you would get in a healthy diet. They help to fill in gaps that are in most people's diets. The best thing is to eat a diet with lots of fruits and vegetables, whole grains, and lean meats, and take a multivitamin.<p>

<b>Q: How do I know which brand of multivitamin to buy?</b><br>
A: You can buy the drugstore brand. It is the same as the more expensive brands, and it is just as good for you. Look for multivitamins that have 100% of the US Recommended Daily Allowance (or RDA) of folic acid. You don't need any more than 100% of the RDA of any nutrient.<br><br>
Make sure that the letters &#8220;USP&#8221; are on the label. This means that the multivitamins have been tested and meet government guidelines. Be sure to check the expiration date.<p>

<b>Q: What does &#8220;time release&#8221; mean?</b><br>
A: Time-release means that the vitamins are released slowly into the body. You don't need time-release multivitamins. They cost more and you don't need to have the nutrients released into your body this way.<p>

<b>Q: When should I take my multivitamin?</b><br>
A: Take your multivitamin every day. It is best to take it with a meal. When you are full, your body absorbs the pill better.<p>

<b>Q: What if I forget to take my multivitamin?</b><br>
A: You do not need to take your multivitamin at the same time every day. If you forget, don't worry. Take it the next time you eat. The best way to remember is to have a routine. For example, you can:
<ul>
<li>take it when you take other medications at breakfast</li>
<li> take it when you have a mid-day snack</li>
<li> put your bottle of multivitamins near something in your kitchen that you use every day</li>
</ul>









<span class="manualHeader"><b>Marty</b></span><br><br>

<b>Out of Breath to Radiation Therapy in Just Six Days</b><p>

We've all got our own stories of how it happened and when we found out about our sickness. Mine was simple: I started losing my breath, and I couldn't get enough air to just walk up a flight of stairs. Just springtime hay fever, right?  Not so lucky. So, before I knew it, I was leaving just two weeks before the end of my first year of college and still more than a month away from my nineteenth birthday. I was just a kid who hadn't even finished my first year of college! <p>

<img src="images/success/marty-v1.gif"><br><br>
Final exams were left untaken and projects left unfinished &#150; I had something more important to do: getting rid of a grapefruit-sized tumor in my chest. Less than six days after I had finally gone to the infirmary, I was 800 miles away, back home and getting my first dose of chemotherapy.  In the end, it took months of chemo and radiation and a bone marrow transplant just to make sure it was all gone.<p>

The experience can't not change you, but for some survivors, the changes are more subtle than others.  For me, the experience brought me more courage and the conviction to do things I never thought I could or would do. My cancer was a big reminder for me that, although it's a clich&#233;, &#8220;Life is too short.&#8221;  It's too short to not ride roller coasters, to not go skydiving, to not marry your true love, and to not explore Europe with nothing but a backpack.  The bottom line is that life is too short to let fear and doubts keep you from living life to the fullest.  All these things and more I've been able to do, only because of the second chance that successful treatment brought me.<p>

Not only did my cancer help give me courage and a second chance to do what I never thought I could, but it reminded me that relationships are more important than anything I could pursue.  First, I learned to set aside my childish differences and really connect with my sister.  She became my closest friend as I learned to share my feelings and fears.  And I allowed myself to deepen my relationship with my parents and learned more about that relationship than I could have ever imagined.  The ultimate lesson and ultimate challenge for me was that the way that I foster and nurture my relationships with my growing family, my community, and my environment are what truly defines me as a person.<p>

I am grateful for my experience and the changes I've gone through.  I've learned to refocus my energy on those around me, rather than just myself.  I've learned that everything worth doing comes with a challenge, but the benefits are well worth the effort. All of these things are possible only because of the second chance at good health that my cancer treatment has brought me.<p>

If there's one thing that getting this second chance at life taught me, it's that the most important things in life are relationships.  I learned that there is nothing more precious and nothing that defines me more than how I nurture the important relationships in my life &#150; those with my growing family, my sister, my friends, and now my wife.  And I don't only cherish my relationships with people &#150; my relationship with nature, with the world around me, and with my spirituality are all precious to me.  Getting a second chance at life taught me and continues to teach me that &#150; and for that, I am eternally grateful.



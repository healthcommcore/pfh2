<span class="tipsHeader">TREATING DEPRESSION</span><br><br>

A lot of smokers who are depressed feel as though a cigarette can lift their spirits. But there are many healthier ways to feel better. Nowadays, there are so many ways to treat depression that there is an appropriate treatment for almost everyone. These include:<br><br>

<a href="#sec2a">Anti-depressant Medications</a><br>
<a href="#sec2b">Counseling</a><br>
<a href="#sec2c">Good Relationships</a><br>
<a href="#sec2d">Activity Scheduling</a><br>
<a href="#sec2e">Stress Reduction Through Relaxation</a><br>
<a href="#sec2f">Complementary and Alternative Medicine</a><br>

<a name="sec2a"></a><br>
<ul>
<li><b>Anti-depressant medications.</b> These prescribed medications are often a very useful part of treating depression. A health care provider works with you to find the one that is best for you. Anti-depressant medications can be used along with counseling. Also, if you're a smoker who's trying to quit, <a href="health_pharmo-zyban.php">Zyban</a> is a prescription medication that contains Wellbutrin, an antidepressant that has also been found to help people quit smoking. Partnership for Health can help you get Zyban free of charge.<br><br>
<a href="#top">back to top</a></li>
</ul>

<a name="sec2b"></a><br>
<ul>
<li><b>Counseling.</b> This involves talking with a counselor or therapist, and it can help many people with depression. Counselors use different techniques to help treat a person with depression. One of these is <a href="health_manual-1g2.php">positive thinking</a>. Sometimes being depressed can make you more likely to notice and remember bad experiences than good ones. Try challenging negative thoughts by thinking about all the events in your life &#150;  both good <b>and</b> bad. Thinking in this new way can help some people feel less depressed.<br><br>
Be sure to find a counselor or therapist who makes you feel comfortable. You can ask your health care provider for a referral or call your community mental health clinic.<br><br>
<a href="#top">back to top</a></li>
</ul>

<a name="sec2c"></a><br>
<ul>
<li><b>Good Relationships.</b> Doctors have found that strong, good relationships can help some people recover from depression.<br><br>
<a href="#top">back to top</a></li>
</ul>

<a name="sec2d"></a><br>
<ul>
<li><b>Activity Scheduling.</b> When people take part in activities that are fun or lead to feelings of accomplishment, they usually feel better. It may help your mood if you are able to schedule these kinds of activities into your day. <a href="health_tips-activity.php">Exercise</a> is a great activity to plan for. Try to get at least 30 minutes of exercise on most days, and more if you can.<br><br>
<a href="#top">back to top</a></li>
</ul>

<a name="sec2e"></a><br>
<ul>
<li><b>Stress Reduction Through Relaxation.</b> Because stress can play a part in depression, relaxation techniques can often help. Visit the <a href="health_tips-relaxation.php">Relaxation Techniques</a> page to learn about:
<ul>
<li>deep breathing</li>
<li>visual imagery</li>
<li>progressive muscle relaxation</li>
<li>meditation</li>
<li>yoga</li>
<li>massage</li>
</ul><br>
<a href="#top">back to top</a></li>
</ul>

<a name="sec2f"></a><br>
<ul>
<li><b>Complementary and Alternative Medicine.</b> Some herbs have been shown to help people with depression. Doctors have different opinions about how well these medicines work. Always talk with your health care provider before using <b>any</b> alternative medicine.<br><br>
<a href="#top">back to top</a></li>
</ul>

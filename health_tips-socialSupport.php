When getting ready to kick the smoking habit, setting up a support network for yourself is a good idea. Help from your friends, family, co-workers, and other people in your life can make all the difference when you're trying to reach your goal. Read the tips below to learn how to get the most support.

<ul>
<li>You don't need to tell everyone you're quitting &#150; just a few people you can trust.</li>
<img src="images/tipsheets/social-support.jpg" width=181 height=121 border=0 alt="" align="right" style="padding-left:20px; padding-top:3px; padding-bottom:16px;">
<li>Think carefully about who your allies are. These are the people you can <b> always </b> call for support. Think of people who will take the time to lift your spirits and distract you from your urge to smoke.</li>
<li>Talk to former smokers. They will know what you're going through and may have good ideas and be able to offer moral support.</li>
<li>Talk about your needs with your family. Be clear about what is helpful (cheering you on and sharing in your successes) and what is not helpful (nagging and making things worse). </li>
<li>Ask your spouse or partner to help you quit. If he or she still smokes, ask that he or she avoid smoking around you. The best place for your spouse or partner to smoke is outside. The next best place is in the garage or in a room with the door closed. Agree ahead of time on what parts of the house will be smoke-free zones.</li>
<li>Ask other smokers not to light up around you.</li>
<li>People may sometimes forget that you are quitting or have quit. You might need to remind them.</li>
<li>Tell the people who are helping how much their support means to you. Let them know how they can continue to help, even when the hardest days are behind you.</li>
</ul>

<a name="thingsToSay"></a>
<br>

<b>Some Things to Say</b><br><br>
You'll find that many of the people in your life will want to help you quit &#150; but they may need to be told the best ways to help. Here are a few things to say to people who help you quit:
<ul>
<li>"In the beginning, I may be on edge. So please bear with me if I seem cranky or crabby for the first few days after I stop smoking. I promise it will go away soon."</li>
<li>"Cheer me on or give me rewards. The rewards don't have to cost much &#150; it's the thought that's important."</li>
<li>"Check in with me from time to time to ask me how things are going."</li>
<li>"If I slip up, don't let me beat myself up. And don't let me give up."</li>
<li>[to a current smoker] "Please don't tempt me &#150; it's hard enough without seeing or smelling cigarettes. It will be really hard if you offer me a cigarette or smoke in front of me."</li>
<li>"Thank you so much for helping me stop smoking. It means a lot to me."</li>
</ul><br>



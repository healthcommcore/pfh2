<span class="manualHeader"><b>Bernadette</b></span><br><br>

I think that everyone wonders about how they would react if they received the news that they had cancer. It changed my life in innumerable ways. Everything that was truly important became crystal clear to me in that split second after I heard those words.<p>

<img src="images/success/bernadette-v1.gif"><br><br>
I have now been in remission for 18 months from acute promyelocytic leukemia [APML]. I was feeling generally unwell. I was just exhausted and constantly out of breath. It took the doctors a few days of tests and a bone-marrow biopsy before they delivered the diagnosis. The leukemic cells were not in my blood, but they were in my bone marrow. I also had a blood clot on one of my lungs. It was a side effect of the APML. I was told countless times that I was very lucky that I had never started smoking, as it would have added so many complications. As it was, I needed an extremely healthy heart and set of lungs to get me through three sessions of chemotherapy.<p>

Having cancer really makes you value your life. It makes you value your health. I have no idea what the future holds or what life has in store for me, but I am hopeful that I can live a long and full life. This hope exists only because of all of those doctors, nurses, and researchers out there who have made the battle against cancer their life's work. Thank you all so much!<p>



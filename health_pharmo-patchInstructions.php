<span class="manualHeader">HOW TO USE YOUR NICOTINE PATCHES</span><br><br>

1. Stop smoking completely before you start using your patches.<br><br>

2. The best time to put on a patch is first thing in the morning. If you can't put it on then, choose a set time each day. Doing it at the same time each day will help you remember.<br><br>

3. Use scissors to open the pouch on the dotted line. Don't take a patch out until you're ready to use it, because it will lose its strength.<br><br>

4. Take the patch out and throw the pouch away. The sticky, silver side of the patch is covered with a clear plastic liner. The liner has a slit down the center to help you peel back one half at a time (like an adhesive bandage). Pull the liner off. Hold the patch by the edges and try not to touch the silver part of the patch. Put the liner pieces in the slot in the disposal tray.<br><br>
5. The patch should go on your upper arm or hip. The area should be clean, dry, and non-hairy. Don't put the patch over areas of the skin that are burned, broken out, cut, oily, or irritated. Make sure the skin doesn't have any lotion, moisturizer, or sunscreen on it.<br><br>
6.	Press the patch to your skin with the heel of your hand for at least 10 seconds. Be sure its edges are firmly stuck to your skin.<br><br>
7.	Wash your hands as soon as you're done. The nicotine from the patch can irritate your eyes and nose.<br><br>
8.	After 16 to 24 hours, take the patch off. Fold it in half so the silver side sticks to itself and put it in the slot in the disposal tray. This is very important, because it keeps it out of reach of children and pets.<br><br>
9.	Wash your hands after you take off the patch.<br><br><br>


<b>If your doctor recommends taking the patch in 3 strengths:</b><br><br>
1. Start with the highest nicotine strength, Step 1 (21 mg), for the first 6 weeks. <br><br>
2. 	After 6 weeks, move to the middle nicotine strength, Step 2 (14 mg), for 2 weeks.<br><br>
3. 	Then, move to the lowest nicotine strength, Step 3 (7 mg), for the final 2 weeks. (If you
still feel the need to use NicoDerm CQ after week 10, talk with your doctor.) <br><br><br>

<b>If your doctor recommends taking the patch in 2 strengths:</b><br><br>
1. 	Start with the middle nicotine strength, Step 2 (14 mg), for 6 weeks.<br><br>
2. 	After 6 weeks, move to the lowest nicotine strength, Step 3 (7 mg), for the final 2
weeks. (If you still feel the need to use NicoDerm CQ after week 8, talk with your doctor.)<br><br>












<span class="manualHeader">QUESTIONS AND ANSWERS ABOUT ZYBAN</span><br><br>

<b>How does Zyban work?</b><br>
Zyban is a non-nicotine, prescription-strength medication to help you quit smoking and lessen your withdrawal symptoms. It's believed to work on chemicals in the brain to help make quitting bearable.<br><br> You will begin taking Zyban about 8 to 14 days before your quit date. When your quit date arrives, you stop smoking and continue taking Zyban for as long as prescribed, which is usually not longer than 3 months. <br><br>

<b>How effective is Zyban?</b><br>
Zyban <b>doubles</b> the chances that you'll quit smoking for good.<br><br>

<b>What can I expect when I quit smoking?</b><br>
You might experience nicotine withdrawal. This is usually strongest during the first 3 to 4 days after quitting. Some people have cravings for nicotine, feel anxious or irritable, or have trouble concentrating. The good news is that Zyban helps lower the urge to smoke and lessens symptoms of withdrawal to make quitting much easier. People who have successfully used Zyban in quitting smoking say that it takes away their desire to smoke and that cigarettes just don't taste as good.<br><br>

<b>What are the side effects to using Zyban?</b><br>
Dry mouth and insomnia are the most common side effects. Shakiness and skin rashes are also possible.<br><br>

<b>Is Zyban right for me?</b><br>
If you have never used medications to help you quit smoking before, you may want to start with the patch or another nicotine replacement therapy (NRT). Zyban may also be combined with nicotine replacement therapies. If you are taking any medication that contains bupropion, most commonly found in anti-depressants, or if you have an allergy to bupropion, do not take Zyban. This medication should not be taken if you have a history of seizures or have had a brain tumor or brain surgery. Also, you should not take Zyban if you have an eating disorder, have abruptly stopped drinking alcohol recently, or take an MAO inhibitor (like Nardil or Parnate). If you are pregnant or nursing, you should not take Zyban.<br><br>

<b>How much does Zyban cost?</b><br>
Nothing! As a member of Partnership for Health, you may request a free 3-month supply.*<br><br>

<b>How can I get Zyban?</b><br>
Read and fill out the consent form to request Zyban. We will contact your doctor to make sure Zyban is right for you. If your doctor agrees, we will send him or her a 3-month supply of Zyban. You will need to go to your doctor to pick it up.<br><br>


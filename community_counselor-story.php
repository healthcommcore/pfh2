<b>Katie's Story</b><br><br>

I was diagnosed with ovarian cancer in my early twenties, and it was completely unexpected. I was having abdominal pain, cramping, and other fun gastrointestinal stuff. Thankfully, I had a very astute and concerned primary care doctor.<p>

<img src="images/katiesStory.jpg" width=345 height=287 border=0 alt=""><br><br>

I was originally diagnosed with Stage IIA cancer. Because children and family were always a priority for me, I went against the recommendation of my oncologist to have a bilateral oophorectomy and hysterectomy (removal of the ovaries and uterus). Instead, I enrolled in a clinical trial. I went into complete remission until April 2004. I was then diagnosed with Stage IIIC cancer (it had spread to my lymph nodes). I again was cautious with treatment, but I did opt for unilateral oophorectomy (removing one ovary only).<p>

Professionally, my survivorship has given me perspective on the important things in life. I'm in medical school now, and while my classmates worry about whether or not they will get a &#8220;high pass&#8221; or simply a &#8220;pass,&#8221;  I spend ample time studying so that I can care for my patients. I also make a lot of time to dedicate to spending with my husband, friends, and family.<p>

Personally, cancer has made me an even stronger person, and helped me realize that people deal with illness differently. My mother, for example, was (and remains) in complete denial and never once would mention cancer or visit me during or after a treatment. My husband and brother, on the other hand, needed to know every little detail.<p>

Cancer has also taught me that it is OK to take the road less travelled and do things on <b>my</b> schedule. I have gotten many questions and comments about why I would ever get pregnant during my third year of med school. Well, when you have slim to little chance of being able to get pregnant and you're already hitting 30, taking a year longer to finish medical school doesn't seem so important.<p>

My goal with Partnership for Health is to help you be as healthy as you want to be. I'll be putting a new message up each week on the site's home page, and I'll also be posting topics in the forum to get everyone talking. I look forward to getting to know everyone in the Partnership for Health  community!<p>

And remember, there is never a Partnership for Health issue too small. If you have any questions, issues, or just information you want to share, please be sure to <a href="mailto:pfh@hcc-web.org"> email me</a>!






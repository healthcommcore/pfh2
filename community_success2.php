<span class="manualHeader"><b>Brian</b></span><br><br>

Having gone through the experience I went through and being a cancer survivor has made me look at the world in a completely different way. There are things that just don't seem as important now. Small things mostly, things that would normally make me upset or aggravated. Having had a second chance at life makes you look at almost everything differently.<p>
My health is so much more important to me now. I no longer take anything for granted. Having a good diet and getting exercise and simply making good choices for my body are so much more important now. People don't realize how much your body changes on the inside when you have spent months or years undergoing chemotherapy and radiation treatments. Your body changes as you get older, and having had treatments for cancer, your body changes even more.<p>
For me, when I was first diagnosed right up until I was safely in remission, I never gave up the fight. No matter how awful I felt (and believe me, I look back and remember days that were absolutely horrible), I never gave up! I always tried to maintain a good sense of humor and most importantly a positive attitude. I know that it really helped me get through the worst times, and there were certainly plenty of those <p>
Music is my biggest passion, so it was my biggest salvation. Without my music, I'm not sure how I would have dealt with any of it. Listening to music helped me get through treatments and it helped me escape the pain, sorrow, and misery I was feeling on an almost daily basis. <p>

I remember that one time, one of my favorite bands was on tour and performing at a club I had frequented prior to my hospitalization. Their new album was one that I would listen to quite often during my treatments. My doctor had told me that as long as my counts were good when that day came, I could go! Well, I ended up going (and ironically, that band broke up later that year), so to this day, it remains one of my most positive memories from that time. I guess what I am trying to say is that you can accomplish a lot if you really put your mind to it.<p>

To this day, I try not to let the little things get in my way. I try to forgive and forget. Life is way too short to not grab hold and enjoy it to its fullest. It can be a very long, slow road to feeling like yourself again. There are days when you just don't see how that day will ever come. I can remember not being able to even write, walk, or open a bottle of soda on my own. OK, so I'm not as fast or as strong as I was.
I lost my long, thick hair and it won't grow back like that again. My circulation isn't what it was, so my fingers and toes are always cold. I feel tired a lot of the time, and I have to get up in the middle of the night several times to answer nature's call, but I am a cancer survivor, and that's something to feel very good about.<p>




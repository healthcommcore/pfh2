Exercise doesn't have to mean joining a fancy gym or jogging every night. There are many types of activities that can help you get the mental and physical benefits of exercise. Try to get at least 30 minutes of activity into your day on five days of the week, and more if you can. <br><br>

<b>Before you begin:</b>
<ul>

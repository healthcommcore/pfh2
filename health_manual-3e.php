<span class="manualHeader">SOOTHING YOUR SYMPTOMS</span><br><br>
When you quit smoking, you'll probably have to deal with withdrawal symptoms. Withdrawing from nicotine can be a challenge, but it's also an important step forward: it means your body is recovering from smoking!<br><br>
Withdrawal symptoms are different for everybody, but there's one thing they all have in common: they don't last long. They usually peak only a few days after quitting, and they often ease up entirely within two weeks. After that, your urge to smoke gets weaker and goes away. <br><br>
Using nicotine patches or Zyban can help you through the tough spots of withdrawal, but make sure you follow the instructions to get the most out of them.<br><br>
Whether or not you're using medication to help you quit, read over the list below for how to handle different types of withdrawal symptoms. Check the ones that you might be worried about, then see how to deal with them. <br><br>


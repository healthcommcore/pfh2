<span class="manualHeader"><b>Char</b></span><br><br>


The day I was diagnosed with a high-grade osteogenic sarcoma of my right distal femur was a whirlwind of activity. I was off for chest X-rays, chest CT, bone scan, bloodwork, and I met with my oncologist. The following week I met with my orthopedic oncologist at Mt. Sinai Hospital in NYC.  Two weeks after my diagnosis, I had surgery to put in a port and started chemotherapy. Limb salvage surgery came next. I was in a long leg cast for two months, and then a custom long leg KFO block brace for another three months. After surgery, I spent more than 10 months on crutches. Then more chemo.<p>

<img src="images/success/char-v1.gif"><br><br>
One could consider me a physical therapy "lifer" at this point. Since my limb salvage surgery, I've had five knee surgeries. Plus,  I've managed to severely sprain my left ankle and have a wild spill off my bike while off-road biking. So I've managed to have a bit of fun that changes up my therapy needs. Takes the focus off this damn knee at times. My orthopedic oncologist says he's ready to "take me out back and shoot me." LOL! I love that guy.<p>

The key is that I am <b>alive</b>. Life now is still filled with a lot of medical "stuff." Most of my time is spent running to doctor appointments and dealing with long-term effects of dealing with cancer. Although, I have two wonderful dogs, Zephyr and Sheba, who I enjoy spending lots of time playing with. I volunteer at a local Gilda's Club that recently opened. It's like a second home to me, very welcoming and inviting. Which is adding purpose to my life.<p>

Even with all the changes that have taken place in my life due to my cancer diagnosis, I wouldn't give it back. Sounds crazy, but it's true.  I've been places, done things, and learned more about myself as a person than I probably would have otherwise.<p>

It is possible to live after an experience with cancer. Maya Angelou wrote, "Surviving is important, but <b>thriving is elegant</b>." <p>



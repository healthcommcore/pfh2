<img src="images/tipsheets/womens-fertility.jpg" width=180 height=233 border=0 alt="" align="right" style="padding-left:20px; padding-top:3px; padding-bottom:16px;">
Although radiation and chemotherapy can save the life of someone with a childhood cancer or similar illness, these treatments may affect a woman's fertility later on. This is usually due to the way the treatments affect the ovaries.<br><br>
The ovaries contain ovum (eggs), which are released during ovulation. The ovaries also make hormones that are necessary for ovulation, menstruation, and pregnancy. If the ovaries are harmed by radiation or chemotherapy, a woman may have late effects like:

<ul>
<li><b>Primary amenorrhea.</b> This is when a woman does not begin to menstruate or does not go through puberty completely.</li>
<li><b>Secondary amenorrhea.</b> This happens when a woman enters menopause before age 40.</li>
<li><b>Irregular hormone levels.</b> A woman may have lower levels of the female hormones that are necessary for a normal menstrual cycle, fertility, and sex drive. </li>
<li><b>Harmed ovum.</b> If the eggs themselves are damaged, a woman can become infertile.</li>
</ul>


Because each situation is unique, radiation and chemotherapy's effects on the reproductive system can be different from one woman to another. However, the effects of radiation generally depend on the dose. The more radiation a woman has, the more fertility may be affected. The effects also depend on the location, or how close the radiation was to the ovaries. <br><br>

The effects of chemotherapy also depend on the dose &#150; the higher the total dose, the more problems there may be with fertility. The type of chemotherapy drugs used is a factor, as well.<br><br>

Timing also plays a part in cancer treatment and fertility issues. Girls treated with chemotherapy drugs before puberty may be affected less than those treated during or after puberty. In some women, the ovaries may recover their function in the months or years after treatment. Some may be fertile in their early childbearing years, but have difficulty conceiving as they get older. <br><br>

<br>
<b>Smoking and Fertility</b><br><br>

Smoking has been proven to harm fertility. If you smoke, you can have more difficulty conceiving than women who don't smoke. Also, if you are prone to these types of treatment effects <b>and</b> you smoke, you may be affecting your fertility even more.<br><br>

Women who have had treatments for illnesses such as Hodgkin's disease or Wilm's tumor are at greater risk for miscarriage, as well as premature or low-birthweight babies. This risk increases even more when a woman smokes.<br><br>

It is important to talk about the details of your treatment with your health care provider, to figure out if your medical history may contribute to problems with fertility.<br><br>



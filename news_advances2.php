<span class="manualHeader">IMPROVEMENTS IN CHEMOTHERAPY</span><br><br>

Many cancers and similar illnesses that affect children are systemic, meaning they affect the entire body. Chemotherapy is an important part of treatment, and newer formulations are being developed that have fewer side effects. New ways of delivering chemotherapy can also help lessen side effects. One example  is continuous infusions of a lower dose on a weekly schedule, instead of a higher dose every 3 weeks.<br><br>

Some cancer cells resist chemotherapy and radiation. Researchers are developing chemical compounds that make these cells more sensitive to radiation and drug treatments. Researchers are also studying the action of cell protectors. These are compounds that protect normal tissue, meaning less damage is done to healthy areas.<br><br>

New biological therapies including interferons, interleukins, and vaccines are being developed to help stimulate the body's antibodies to fight cancer and to help it recover more quickly from the effects of anti-cancer drugs. Antibodies are chemicals that specifically recognize proteins on a cell's surface. More and more, doctors have found certain antibodies useful in targeting proteins on the surface of the cancer cells. <br><br>


